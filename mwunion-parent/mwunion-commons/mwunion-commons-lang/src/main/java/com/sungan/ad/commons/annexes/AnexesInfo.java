public class AnexesInfo {
    private String name;
    private String url;
    private String anexNo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getAnexNo() {
        return anexNo;
    }

    public void setAnexNo(String anexNo) {
        this.anexNo = anexNo;
    }
}
